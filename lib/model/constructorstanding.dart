class ConstructorStanding {
  String position;
  String points;
  String wins;
  String constructorId;
  String name;
  String nationality;

  ConstructorStanding(String position, String points, String wins, String constructorId, String name, String nationality) {
    this.position = position;
    this.points = points;
    this.wins = wins;
    this.constructorId = constructorId;
    this.name = name;
    this.nationality = nationality;
  }
}