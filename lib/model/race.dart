class Race {
  String season;
  String round;
  String url;
  String name;
  String date;
  String time;

  Race(String season, String round, String url, String name, String date, String time) {
    this.season = season;
    this.round = round;
    this.url = url;
    this.name = name;
    this.date = date;
    this.time = time;
  }
}